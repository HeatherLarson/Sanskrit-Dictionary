export function searchDictionary(query: string, category?: Category): SanskritWord[] {
  const normalizedQuery = query.toLowerCase().trim();

  return sanskritDictionary.filter((word) => {
    const matchesCategory = !category || word.category === category;
    const matchesQuery =
      !normalizedQuery ||
      word.sanskrit.toLowerCase().includes(normalizedQuery) ||
      word.transliteration.toLowerCase().includes(normalizedQuery) ||
      word.meaning.toLowerCase().includes(normalizedQuery) ||
      word.devanagari.includes(normalizedQuery);

    return matchesCategory && matchesQuery;
  });
}

export function getWordsByCategory(category: Category): SanskritWord[] {
  return sanskritDictionary.filter((word) => word.category === category);
}

export function getRelatedWords(wordId: string): SanskritWord[] {
  const word = sanskritDictionary.find((w) => w.id === wordId);
  if (!word?.related) return [];

  return sanskritDictionary.filter((w) =>
    word.related?.some(
      (r) =>
        w.sanskrit.toLowerCase().includes(r.toLowerCase()) ||
        w.transliteration.toLowerCase().includes(r.toLowerCase())
    )
  );
}
